package com.zt.capacity.jinan_zwt.bean;

import java.io.Serializable;


/**
 * The persistent class for the zt_data_action database table.
 *
 */
public class DataActionBean implements Serializable {
	private static final long serialVersionUID = 1L;

	//命令id
	private int actionId;

	//下发类型
	private int actionType;

	//下发值
	private int actionValue;

	//车牌号码
	private String carPass;

	//0表示未处理，1表示处理成功，-1表示处理失败
	private Integer dealResult;

	//网关接收处理时间
	private String dealTime;

	//抓拍保存 只保存图片名 且不带后缀 多图片则用（，）隔开
	private String imgPath;

	//手机号码
	private String phoneNumber;

	//0表示网关未接收，1表示接收，-1表示发送接收数据失败
	private Integer receiveResult;

	//重发次数
	private int resendCount;

	//下发人
	private String sendPerson;

	//下发备注
	private String sendRemark;

	//终端接收数据时间
	private String storeTime;

	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public int getActionId() {
		return actionId;
	}

	public void setActionId(int actionId) {
		this.actionId = actionId;
	}

	public int getActionType() {
		return actionType;
	}

	public void setActionType(int actionType) {
		this.actionType = actionType;
	}

	public int getActionValue() {
		return actionValue;
	}

	public void setActionValue(int actionValue) {
		this.actionValue = actionValue;
	}

	public String getCarPass() {
		return carPass;
	}

	public void setCarPass(String carPass) {
		this.carPass = carPass;
	}

	public Integer getDealResult() {
		return dealResult;
	}

	public void setDealResult(Integer dealResult) {
		this.dealResult = dealResult;
	}

	public String getDealTime() {
		return dealTime;
	}

	public void setDealTime(String dealTime) {
		this.dealTime = dealTime;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public Integer getReceiveResult() {
		return receiveResult;
	}

	public void setReceiveResult(Integer receiveResult) {
		this.receiveResult = receiveResult;
	}

	public int getResendCount() {
		return resendCount;
	}

	public void setResendCount(int resendCount) {
		this.resendCount = resendCount;
	}

	public String getSendPerson() {
		return sendPerson;
	}

	public void setSendPerson(String sendPerson) {
		this.sendPerson = sendPerson;
	}

	public String getSendRemark() {
		return sendRemark;
	}

	public void setSendRemark(String sendRemark) {
		this.sendRemark = sendRemark;
	}

	public String getStoreTime() {
		return storeTime;
	}

	public void setStoreTime(String storeTime) {
		this.storeTime = storeTime;
	}
}