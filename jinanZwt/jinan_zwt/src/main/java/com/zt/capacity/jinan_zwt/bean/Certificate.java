package com.zt.capacity.jinan_zwt.bean;

import java.io.Serializable;

/**
 * 小证信息
 * Created by Administrator on 2018/5/11.
 */

public class Certificate implements Serializable {

    private String proName;//工地名称
    private String constructionAddress;//施工地址
    private String enterpriseName;//运输公司
    private String plateNumber;//车牌号
    private String startTime;//有效期起
    private String endTime;//有效期止
    private String licStartTime;//开工时间
    private String licEndTime;//停工时间
    private String unloadingName;//消纳场

    public String getProName() {
        return proName;
    }

    public void setProName(String proName) {
        this.proName = proName;
    }

    public String getConstructionAddress() {
        return constructionAddress;
    }

    public void setConstructionAddress(String constructionAddress) {
        this.constructionAddress = constructionAddress;
    }

    public String getEnterpriseName() {
        return enterpriseName;
    }

    public void setEnterpriseName(String enterpriseName) {
        this.enterpriseName = enterpriseName;
    }

    public String getPlateNumber() {
        return plateNumber;
    }

    public void setPlateNumber(String plateNumber) {
        this.plateNumber = plateNumber;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getLicStartTime() {
        return licStartTime;
    }

    public void setLicStartTime(String licStartTime) {
        this.licStartTime = licStartTime;
    }

    public String getLicEndTime() {
        return licEndTime;
    }

    public void setLicEndTime(String licEndTime) {
        this.licEndTime = licEndTime;
    }

    public String getUnloadingName() {
        return unloadingName;
    }

    public void setUnloadingName(String unloadingName) {
        this.unloadingName = unloadingName;
    }
}
